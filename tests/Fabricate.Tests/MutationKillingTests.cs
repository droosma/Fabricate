    [Fact]
    public void FactoryGenerator_EmitsExactSource_ForTwoBuilders()
    {
        var source = """
using System;
using Fabricate;

namespace TestApp;

public class Patient
{
    public string Name { get; set; } = "";
}

public record Appointment(string DoctorName, DateTime ScheduledAt);

[Fabricate<Patient>]
public partial class PatientBuilder
{
    private static partial Patient ValidInstance() => new() { Name = "John" };
}

[Fabricate<Appointment>]
public partial class AppointmentBuilder
{
    private static partial Appointment ValidInstance() => new("Dr. Smith", new DateTime(2026, 1, 1));
}
""";

        var generated = GenerateSource(source, "TestApp.A.g.cs", new BuilderGenerator(), new FactoryGenerator());

        var expected = BuildExpectedSource(
            "// <auto-generated />",
            "#nullable enable",
            string.Empty,
            "namespace TestApp;",
            string.Empty,
            "public static partial class A",
            "{",
            "    public static global::TestApp.PatientBuilder Patient => new();",
            "    public static global::TestApp.AppointmentBuilder Appointment => new();",
            "}");

        generated.Should().Be(expected);
    }

