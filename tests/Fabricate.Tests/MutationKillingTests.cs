    [Fact]
    public void BuilderGenerator_EmitsExactSource_ForSimpleSettableClass()
    {
        var source = """
using Fabricate;

namespace TestApp;

public class Patient
{
    public string Name { get; set; } = "";
    public int Age { get; set; }
}

[Fabricate<Patient>]
public partial class PatientBuilder
{
    private static partial Patient ValidInstance() => new() { Name = "John", Age = 30 };
}
""";

        var generated = GenerateSource(source, "PatientBuilder.g.cs", new BuilderGenerator());

        var expected = BuildExpectedSource(
            "// <auto-generated />",
            "#nullable enable",
            string.Empty,
            "namespace TestApp;",
            string.Empty,
            "partial class PatientBuilder",
            "{",
            "    private static partial global::TestApp.Patient ValidInstance();",
            string.Empty,
            "    private string _name = default!;",
            "    private int _age = default!;",
            string.Empty,
            "    public PatientBuilder()",
            "    {",
            "        var seed = ValidInstance();",
            "        _name = seed.Name;",
            "        _age = seed.Age;",
            "    }",
            string.Empty,
            "    public global::TestApp.Patient Build()",
            "        => new global::TestApp.Patient()",
            "        {",
            "            Name = _name,",
            "            Age = _age,",
            "        };",
            string.Empty,
            "    public static implicit operator global::TestApp.Patient(PatientBuilder builder)",
            "        => builder.Build();",
            string.Empty,
            "    public PatientBuilder WithName(string name)",
            "    {",
            "        _name = name;",
            "        return this;",
            "    }",
            string.Empty,
            "    public PatientBuilder WithAge(int age)",
            "    {",
            "        _age = age;",
            "        return this;",
            "    }",
            string.Empty,
            "}");

        generated.Should().Be(expected);
    }

