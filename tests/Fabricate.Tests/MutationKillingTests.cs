    private static string GenerateSource(string source, string fileName, params IIncrementalGenerator[] generators)
    {
        var compilation = TestHarness.CreateCompilation(source);
        var result = TestHarness.RunGenerators(compilation, generators);
        var generatedSources = TestHarness.GetGeneratedSources(result);

        generatedSources.Should().ContainKey(fileName);
        return generatedSources[fileName];
    }

    private static string BuildExpectedSource(params string[] lines)
    {
        var builder = new StringBuilder();
        foreach (var line in lines)
        {
            builder.AppendLine(line);
        }

        return builder.ToString();
    }
